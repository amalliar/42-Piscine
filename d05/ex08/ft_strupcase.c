char	*ft_strupcase(char *str)
{
	char	*p_str;

	p_str = str;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
			*str = *str - 'a' + 'A';
		str++;
	}
	return (p_str);
}
